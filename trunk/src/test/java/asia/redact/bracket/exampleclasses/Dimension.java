package asia.redact.bracket.exampleclasses;

public class Dimension extends ExampleBase {

	int width, height;
	
	public Dimension() {
		
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}
	
	

}
